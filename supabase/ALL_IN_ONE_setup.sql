CREATE TABLE IF NOT EXISTS public.rental_categories (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  brand_id      BIGINT REFERENCES public.rental_brands(id) ON DELETE CASCADE,
  name          TEXT NOT NULL,
  description   TEXT,
  display_order INTEGER NOT NULL DEFAULT 0,
  is_active     BOOLEAN NOT NULL DEFAULT true,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  created_by    TEXT,
  updated_by    TEXT
);

CREATE TABLE IF NOT EXISTS public.rental_products (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  brand_id      BIGINT REFERENCES public.rental_brands(id) ON DELETE SET NULL,
  category_id   BIGINT REFERENCES public.rental_categories(id) ON DELETE SET NULL,
