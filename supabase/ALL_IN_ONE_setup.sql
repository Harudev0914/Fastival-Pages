-- C. 렌탈 — 주문(결제) / 입점 문의(중고매입)
-- =====================================================================
CREATE TABLE IF NOT EXISTS public.rental_orders (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id     BIGINT REFERENCES public.rental_products(id) ON DELETE SET NULL,
  product_name   TEXT,
  brand_name     TEXT,
  option_name    TEXT,
  customer_name  TEXT,
  customer_phone TEXT,
  customer_email TEXT,
  rental_start   TIMESTAMPTZ,
  rental_days    INTEGER NOT NULL DEFAULT 1,
  rental_end     TIMESTAMPTZ,
  quantity       INTEGER NOT NULL DEFAULT 1,
  daily_price    NUMERIC NOT NULL DEFAULT 0,
  deposit        NUMERIC NOT NULL DEFAULT 0,
  delivery_fee   NUMERIC NOT NULL DEFAULT 0,
  total_amount   NUMERIC NOT NULL DEFAULT 0,
  payment_status TEXT NOT NULL DEFAULT 'paid'  CHECK (payment_status IN ('pending','paid','cancelled','refunded')),
  payment_method TEXT,
  payment_id     TEXT,
  order_status   TEXT NOT NULL DEFAULT 'reserved' CHECK (order_status IN ('reserved','renting','returned','cancelled')),
  memo           TEXT,
  created_at     TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_at     TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_by     TEXT
);

CREATE TABLE IF NOT EXISTS public.rental_purchase_inquiries (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id      BIGINT REFERENCES public.rental_products(id) ON DELETE SET NULL,
  product_name    TEXT NOT NULL,
  brand_name      TEXT,
  condition_grade TEXT NOT NULL DEFAULT 'A' CHECK (condition_grade IN ('C','B','A','A+','A++')),
  desired_price   NUMERIC NOT NULL DEFAULT 0,
  images          JSONB NOT NULL DEFAULT '[]'::jsonb,
  description     TEXT,
  applicant_name  TEXT,
  applicant_phone TEXT,
  applicant_email TEXT,
  status          TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending','approved','hold','rejected')),
  admin_memo      TEXT,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_by      TEXT
);

CREATE INDEX IF NOT EXISTS idx_rental_orders_product ON public.rental_orders (product_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_rental_orders_status  ON public.rental_orders (order_status, payment_status);
CREATE INDEX IF NOT EXISTS idx_rental_purchase_status ON public.rental_purchase_inquiries (status, created_at DESC);

DROP TRIGGER IF EXISTS trg_rental_orders_updated ON public.rental_orders;
CREATE TRIGGER trg_rental_orders_updated BEFORE UPDATE ON public.rental_orders
  FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
DROP TRIGGER IF EXISTS trg_rental_purchase_updated ON public.rental_purchase_inquiries;
CREATE TRIGGER trg_rental_purchase_updated BEFORE UPDATE ON public.rental_purchase_inquiries
  FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();

ALTER TABLE public.rental_orders             ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.rental_purchase_inquiries ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "ro insert"  ON public.rental_orders;
