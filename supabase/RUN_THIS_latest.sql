-- =====================================================================
-- 5) 계약서(문서) 관리
-- =====================================================================
CREATE TABLE IF NOT EXISTS public.contracts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  template TEXT NOT NULL CHECK (template IN ('freelancer','construction_order','event_order','rental_handover','rental')),
  title TEXT NOT NULL, customer_name TEXT,
  data JSONB NOT NULL DEFAULT '{}'::jsonb,
  status TEXT NOT NULL DEFAULT 'draft' CHECK (status IN ('draft','completed')),
  inquiry_id BIGINT, owner_user_id UUID,
  created_at TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT TIMEZONE('utc'::text, NOW()), created_by TEXT, updated_by TEXT
);
CREATE INDEX IF NOT EXISTS idx_contracts_template ON public.contracts (template, created_at DESC);
DROP TRIGGER IF EXISTS trg_contracts_updated ON public.contracts;
CREATE TRIGGER trg_contracts_updated BEFORE UPDATE ON public.contracts FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();
ALTER TABLE public.contracts ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "contracts admin" ON public.contracts;
DROP POLICY IF EXISTS "contracts owner read" ON public.contracts;
CREATE POLICY "contracts admin" ON public.contracts FOR ALL TO authenticated USING (true) WITH CHECK (true);
CREATE POLICY "contracts owner read" ON public.contracts FOR SELECT TO authenticated USING (owner_user_id = auth.uid());
GRANT ALL ON public.contracts TO authenticated;

-- =====================================================================
-- 6) 시공 업무/업체/견적서 트리거 + RLS (관리자 전용)
-- =====================================================================
DO $$
DECLARE t TEXT;
BEGIN
  FOREACH t IN ARRAY ARRAY['construction_companies','construction_works','estimates'] LOOP
    EXECUTE format('DROP TRIGGER IF EXISTS trg_%s_updated ON public.%I;', t, t);
    EXECUTE format('CREATE TRIGGER trg_%s_updated BEFORE UPDATE ON public.%I FOR EACH ROW EXECUTE PROCEDURE public.set_updated_at();', t, t);
    EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY;', t);
    EXECUTE format('DROP POLICY IF EXISTS "%s admin" ON public.%I;', t, t);
    EXECUTE format('CREATE POLICY "%s admin" ON public.%I FOR ALL TO authenticated USING (true) WITH CHECK (true);', t, t);
    EXECUTE format('GRANT ALL ON public.%I TO authenticated;', t);
  END LOOP;
END $$;

